		<h3> Topaz Travels Dazzles Akwaaba 2018 with <em>Kulikuli </em>and <em>Kilishi</em> </h3>
						<p>
Breaking away from the regular tradition of toffee-on-the-table, Topaz Travels went all Nigerian with a welcome tray of kulikuli and kilishi at the 14th Akwaaba African travel market 2018.
Visitors to the stand were treated to delicious snacks of kulikuli made from deep fried spiced groundnut paste, and kilishi made from dry deboned meat.
<br> Susan Akporiaye, MD Topaz Travels, shared the recipe and nutritional value of the snacks, pointing out that what is most amazing about kulikuli and kilishi is that they contain no artificial preservatives and can be kept for months without losing taste. </p>
								
							<p>She explained that Nigeria is a heaven of delicacies relying fully on natural ingredients which have drawn many tourists from all over the world to sample the variety of Nigerian dishes from the north to the south of the country.
With the increase in popularity of Nigerian food tourism among foreign tourists, Topaz Travels takes the tourist on an excursion into Nigerian food and culture through a selection of dinners and lunches expertly put together by culinary artisans. </p>
								<p> Visitors could book an exclusive food tour or combine with other itineraries from the array of domestic tour packages Topaz Travels offers.  On the southern circuit, the visitor has a choice of such itineraries as the <em>city &amp; gorilla</em> trek which offers tourists a city tour of Calabar the home of the largest street cultural festival in Africa- the Calabar Carnival. This itinerary takes the tourist to relics of African indigenous civilizations, early Christian missions, slave trade centres and the local cults. Visitors also experience the pristine Cross River rainforest, one of the best in the world, on a walkway running through the forest canopy, 25 metres above the jungle floor watching gorillas in their natural mountain habitat. Visitors are also able to see a variety of primates, including magnificent Chimpanzee at the Afi Mountain Wildlife Sanctuary which protects the populations of several endangered species most notable of which are Gorilla dienli which is unique to this area and the wild drill monkey, another endangered species native to this area. </p>
								<p> Alternatively, Northwards of the country, Topaz offers <em>Elephanta safari,</em> a game viewing tour of Sumu Wildlife Park and the famous Yankari Game Reserve. The fun drive commences from the picturesque city of Abuja to the rainforest of Yankari Games Resort, the country's richest wildlife oasis. The resort accommodates the largest surviving elephant population in Nigeria, one of the largest remaining in West Africa and is home to several natural warm water springs as well as a wide variety of flora and fauna. Located in the heartland of the West African savanna The Yankari Game Resort provides game watching in its natural habitat. In this vast village to wildlife, animals such as waterbucks, bushbucks, oribi, crocodiles, hippopotamus, roan gazelle, wild ox and countless species of monkeys can be spotted.
These and many more wide selection of delightful packages have made Topaz Travels the preferred tour operator for the discerning tourist wishing to experience Nigeria.
</p>

