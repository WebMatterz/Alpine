            <br>
            Seychelles lies in the Indian Ocean, 1600kms off the east coast of Africa, between 4 and 10 ° south of the Equator.  Amongst the many islands the inner isles are largely granitic and situated on the Seychelles Plateau, Mahe, Praslin and La Digue and their neighbouring islands are the most frequented, offering the widest range of accommodation facilities, Frégate, Bird island and Denis, are a little more isolated and still fall under the inner islands group. The Outer islands located beyond the Seychelles Plateau; consist of mainly low-lying sand cays and coral atolls. Within this group only Desroches Island offers accommodation at present.  Desroches and Alphonse are renowned for fly-fishing.   
            <br>
