            <br>
            Uganda can be described as basin shaped rising between the western and eastern blocks of the rift valley. The country lies across the equator bordered by Tanzania in the south, Sudan in the north, Kenya in the east, Zaire in the west and Rwanda in the southwest. The topography of the country is generally flat with an altitude of 1,000m in most parts of the country, however its location on the great African plateau and across the equator gives it a remarkable biological and physical diversity. 
            <br>
            Uganda is naturally endowed with water and fertility, 25% of the area is covered with lakes, rivers and swamps, Lake Victoria being the largest lake in Africa, the source of the River Nile the world's longest river, Lake Kyoga in the center of the country, Lakes Edward, George and Albert close to the border of Congo. The country is also a product of the African Continent uplift with the most mountainous part in the southwest that harbors the Rwenzori mountains which form the highest mountain in Africa, other mountains also include Mount Elgon, Virunga, Bufumbira Mountains on the southwestern border of Rwanda, the Northern Volcanoes of Uganda and many more of these physical features make Uganda a n interesting geographical part of Africa. The country's temperatures range between 26o and 30o during the day and 16o and 20o at night with the hottest months being January, February and December. Apart from some northern parts of the country that receive average annual rainfall as low as 100mm, the rest of the country is green throughout the year with an annual rainfall between 1,000mm to 2,000mm with the coolest season being mid-September to November, the March to May. 
