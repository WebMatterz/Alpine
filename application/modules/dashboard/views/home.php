  <strong> COUNTRY AT A GLANCE: TANZANIA </strong>
          <br>
            Tanzania is one of the unique destinations on the African continent that has yet to be discovered by many. It is a land of many wonders, with an un-paralleled diversity of fauna and flora. Kilimanjaro, the highest permanently snow-capped free standing mountain in Africa, the exotic Islands of Zanzibar, the finest game sanctuaries of Serengeti, Tarangire, Lake Manyara, Ngorongoro Crater, Ruaha, Selous and the Marine Park of Mafia Island are only but a few of the living examples. The scenery, topography and very friendly people harbor the growth of excellent cultural tourism, beach holidays, game hunting, historical and archaeological ventures – and certainly the best wildlife photographic safaris on the continent. 
          <br>
          Tanzania is known for its vastness, its remote southern national parks and also for the parks scenic beauty as well as their abundance in wildlife. To the north of the country lies Ngorongoro Conservation Area (NCA), about 828 Sq. Km, a perfect caldera of the world 18km in diameter. It borders Serengeti National Park, which arguably has the greatest concentration of wildlife in the world. 
          <br>
          Located in south-east Tanzania in a remote and little-visited part of the country, the Selous Game Reserve is Africa’s largest protected wildlife reserve and covers more than 5% of Tanzania’s total area.
            <br>
            Other National Parks in the south include Mahale (the haven of the chimpanzees and baboons); Mikumi, Mkomazi, rubondo, Katavi and Juvenile Udzungwa, rich in plant diversity and in colobus monkeys. 
            <br>
            <br>
            <strong> GENERAL INFORMATION - TANZANIA </strong> <br>
            Tanzania is country so wealthy that it would practically take years to document all the resources. Not only is the country proud to bear witness to the highest and largest free standing mountain in the world but also to the rich and diverse wildlife concentrations, mineral and other resources available. If Africa’s tourism opportunities were to be summarized by one single country that country would be Tanzania. 
